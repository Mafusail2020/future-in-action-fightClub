def main() -> None:
    supabase = get_supabase()

    print("Upserting cities…")
    city_rows = [
        {"slug": slug, "name": name, "country": country,
         "lat": lat, "lng": lng, "population": population}
        for slug, name, country, lat, lng, population in CITIES
    ]
    res = supabase.table("cities").upsert(city_rows, on_conflict="name,country").execute()
    id_by_slug = {row["slug"]: row["id"] for row in res.data}
    print(f"  {len(id_by_slug)} cities")

    existing = supabase.table("solution_cases").select("city_id, title").execute()
    existing_titles = {(row["city_id"], row["title"]) for row in (existing.data or [])}

    for case in CASES:
        city_id = id_by_slug[case["city"]]
        if (city_id, case["title"]) in existing_titles:
            print(f"  skip (exists): {case['title']}")
            continue

        inserted = supabase.table("solution_cases").insert({
            "city_id": city_id,
            "problem_domain": case["problem_domain"],
            "title": case["title"],
            "problem_summary": case["problem_summary"],
            "solution_summary": case["solution_summary"],
            "outcome": case["outcome"],
            "cost_estimate": case.get("cost_estimate"),
            "year_start": case.get("year_start"),
            "year_end": case.get("year_end"),
            "source_urls": case.get("source_urls", []),
            "full_text": case["full_text"],
        }).execute()
        case_id = inserted.data[0]["id"]

        # Embed summary + body so solutions_search finds the case semantically.
        chunks = chunk_text(f"{case['title']}\n\n{case['solution_summary']}\n\n{case['full_text']}")
        embeddings = embed_texts(chunks)
        supabase.table("solution_chunks").insert([
            {
                "case_id": case_id,
                "city_id": city_id,
                "problem_domain": case["problem_domain"],
                "chunk_index": i,
                "content": chunk,
                "embedding": embedding,
            }
            for i, (chunk, embedding) in enumerate(zip(chunks, embeddings))
        ]).execute()
        print(f"  seeded: {case['title']} ({len(chunks)} chunks)")

    print("Done. The world map and solutions_search now have demo data.")


if __name__ == "__main__":
    main()
